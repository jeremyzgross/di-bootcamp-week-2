class Point():
    def __init__(self, x, y):
        self.x = x
        self.y = y

## create an instance of the class
p = Point(3,4) #assigning 3 to x attribute and 4 to y attribute

## access the attributes
print("p.x is:", p.x) #print the x method in the p object
print("p.y is:", p.y) #print the y method in the p object

#Point is the class
#self, x,y is the constructors
#self.x and self.y is the method
#p is the object 

 


